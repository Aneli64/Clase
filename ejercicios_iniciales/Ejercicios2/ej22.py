'''Pedir el nombre y los dos apellidos (nombre, apellido1 y apellido2) de una persona y
mostrar las iniciales.
'''

nombre = input("Introduzca su nombre: ")
apellido1 = input("Introduzca su primer apellido: ")
apellido2 = input("Introduzca su segundo apellido: ")

print("Primera letra del nombre --> ", nombre[0], "\n"
      "Primera letra del primer apellido --> ", apellido1[0], "\n"
      "Primera letra del segundo apellido --> ", apellido2[0])